/**
 * Obtenir le dictionnaire d'une variante spécifique
 */
export function getVariantDictionary(variantCode) {
  // Mapping des codes de variantes vers les dictionnaires
  const variantMappings = {
    'pt-BR': PORTUGUESE_VARIANTS['pt-BR'],
    'pt-PT': PORTUGUESE_VARIANTS['pt-PT'],
    'fr-CA': FRENCH_VARIANTS['fr-CA'],
    'fr-BE': FRENCH_VARIANTS['fr-BE'],
    'fr-CH': FRENCH_VARIANTS['fr-CH'],
    'es-MX': SPANISH_VARIANTS['es-MX'],
    'es-AR': SPANISH_VARIANTS['es-AR'],
    'es-CO': SPANISH_VARIANTS['es-CO']
  };
  
  return variantMappings[variantCode] || null;
}

/**
 * Obtenir la traduction croisée entre deux variantes
 */
export function getCrossVariantTranslation(text, fromVariant, toVariant) {
  const fromDict = getVariantDictionary(fromVariant);
  const toDict = getVariantDictionary(toVariant);
  
  if (!fromDict || !toDict) {
    return null;
  }
  
  const textLower = text.toLowerCase();
  
  // Chercher directement dans le dictionnaire source
  if (fromDict[textLower]) {
    // Si c'est de la même famille linguistique, chercher l'équivalent
    const fromBase = fromVariant.split('-')[0];
    const toBase = toVariant.split('-')[0];
    
    if (fromBase === toBase) {
      // Même langue de base, chercher la traduction spécifique
      const sourceTranslation = fromDict[textLower];
      
      // Chercher l'équivalent dans la variante cible
      for (const [key, value] of Object.entries(toDict)) {
        if (key === textLower || value === sourceTranslation) {
          return {
            translation: value,
            confidence: 0.9,
            method: 'cross_variant_dictionary'
          };
        }
      }
    }
  }
  
  // Utiliser la fonction existante
  return getVariantTranslation(text, fromVariant, toVariant);
}

/**
 * Obtenir toutes les variantes supportées
 */
export function getSupportedVariants() {
  return [
    'pt-BR', 'pt-PT',
    'fr-CA', 'fr-BE', 'fr-CH',
    'es-MX', 'es-AR', 'es-CO'
  ];
}

/**
 * Vérifier si une variante est supportée
 */
export function isVariantSupported(variantCode) {
  return getSupportedVariants().includes(variantCode);
}

/**
 * Obtenir les variantes d'une langue de base
 */
export function getLanguageVariants(baseLanguage) {
  return getSupportedVariants().filter(variant => 
    variant.startsWith(baseLanguage + '-') || variant === baseLanguage
  );
}

/**
 * Détecter la langue de base d'une variante
 */
export function getBaseLanguage(variantCode) {
  return variantCode.split('-')[0];
}

